import mongoose from "mongoose";

// Define the Cart schema
const cartSchema = new mongoose.Schema(
  {
    userId: {
      type: mongoose.Schema.Types.ObjectId,
      ref: "User",
      required: true,
    },
    items: [
      {
        supplementId: {
          type: mongoose.Schema.Types.ObjectId,
          ref: "Supplement",
          required: true,
        },
        quantity: {
          type: Number,
          required: true,
          min: 1,
        },
        price: {
          type: Number,
          required: true,
        },
      },
    ],
    totalPrice: {
      type: Number,
      default: 0,
    },
  },
  {
    timestamps: true,
  }
);

// Export the Cart model
const Cart = mongoose.model("Cart", cartSchema);
export default Cart;
